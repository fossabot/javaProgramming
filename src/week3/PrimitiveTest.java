package week3;

public class PrimitiveTest {
	
	public static void main(String[] args)
	 {
		
		 float f1 = 1;
		 float f2 = 1.0f;
	
		 double d1 = 1.0;
		 double d2 = 1.0d;
		 
		 long l1 = 1;
		 long l2 = 1l;
		 
		 System.out.println(f1);
		 System.out.println(f2);
		 
		 System.out.println(d1);
		 System.out.println(d2);
		 
		 System.out.println(l1);
		 System.out.println(l2);
	 }
}
