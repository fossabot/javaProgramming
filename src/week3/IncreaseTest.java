package week3;

public class IncreaseTest {
	
	 public static void main(String[] args) {
		 
		 int x = 10, y = 10;
		 
		 System.out.println("x = " + x);
		 System.out.println("++x = " + ++x);
		 System.out.println("y = " + y);
		 System.out.println("y++ = " + y++);
		 System.out.println("x = " + x + ", y = " + y);
	}
}